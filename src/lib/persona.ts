export function detectMood(input: string): Mood {
  const text = (input || "").trim();
  if (!text) return "chill";
  for (const rule of RULES) {
    if (rule.keywords.test(text)) return rule.mood;
  }
  return "chill";
}

export function pickReply(mood: Mood, seed?: number): string {
  const pool = REPLIES[mood];
  const i =
    typeof seed === "number"
      ? Math.abs(Math.floor(seed)) % pool.length
      : Math.floor(Math.random() * pool.length);
  return pool[i];
}

export function generate(input: string): { mood: Mood; reply: string } {
  const mood = detectMood(input);
  const seed = hashSeed(input);
  return { mood, reply: pickReply(mood, seed) };
}

function hashSeed(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}
